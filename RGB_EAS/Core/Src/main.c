void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
 // Check which timer triggered this callback
 if (htim == &htim16) // if the triggered timer was Timer 16
 {
 int ADC_RANGE = 4096; // 2^12 (12-bit resolution)
 int PWM_PERIOD = 40000;
 // Start ADC Conversions
 HAL_ADC_Start(&hadc1);
 HAL_ADC_Start(&hadc2);
 // Wait for ADC conversions to complete
 HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY);
 HAL_ADC_PollForConversion(&hadc2, HAL_MAX_DELAY);
 // Read ADC values
 uint16_t xjoy_measurement = HAL_ADC_GetValue(&hadc1);
 uint16_t yjoy_measurement = HAL_ADC_GetValue(&hadc2);
 // Convert ADC levels to a fraction of total
 	 x_loc = 480 - xjoy_measurement*480/ADC_RANGE;
 	 y_loc = 320 - yjoy_measurement*320/ADC_RANGE;

 	TIM3->CCR1 = (int) (blue_int * PWM_PERIOD); // blue
 	 TIM3->CCR3 = (int) (green_int * PWM_PERIOD); // green
 	 TIM3->CCR4 = (int) (red_int * PWM_PERIOD); // red
 }
}
